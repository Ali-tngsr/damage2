"""
ماژول درون‌یابی خواص مواد برای پروژه UMCM-2025-1979
سازگار با Python 2.7 (Abaqus API)
"""

def get_properties(vf_value):
    """
    درون‌یابی خطی خواص مواد بر اساس کسر حجمی (Vf)
    داده‌ها بر اساس جدول ۱ مقاله استخراج شده‌اند [cite: 386]
    """
    
    # داده‌های جدول ۱ [cite: 386]
    # Vf(%), E11, nu12, E22, nu23, YT, G12, G23
    data = [
        [0.0, 1.70, 0.40, 1.70, 0.40, 20.0, 0.61, 0.61],
        [15.0, 12.20, 0.64, 2.48, 0.34, 19.0, 0.81, 0.78],
        [30.0, 22.73, 0.59, 3.25, 0.34, 18.0, 1.09, 1.02],
        [45.0, 33.29, 0.54, 4.45, 0.32, 17.0, 1.51, 1.43],
        [60.0, 43.80, 0.49, 6.59, 0.28, 16.0, 2.21, 2.21],
        [75.0, 54.31, 0.45, 10.89, 0.23, 15.0, 3.68, 3.90],
        [90.0, 64.85, 0.40, 17.98, 0.16, 14.0, 6.15, 6.80]
    ]

    # محدودسازی (Clamping) برای جلوگیری از خطای اکستراپولاسیون 
    vf = max(0.0, min(90.0, float(vf_value)))

    # پیدا کردن بازه درون‌یابی
    for i in range(len(data) - 1):
        if data[i][0] <= vf <= data[i+1][0]:
            v1 = data[i]
            v2 = data[i+1]
            
            # نسبت فاصله (t)
            t = (vf - v1[0]) / (v2[0] - v1[0])
            
            # محاسبه ویژگی‌ها (Interpolation)
            props = [v1[j] + t * (v2[j] - v1[j]) for j in range(1, 8)]
            
            return {
                'E11': float(props[0]),
                'nu12': float(props[1]),
                'E22': float(props[2]),
                'nu23': float(props[3]),
                'YT': float(props[4]),
                'G12': float(props[5]),
                'G23': float(props[6])
            }
    return None

